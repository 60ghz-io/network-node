// Optional simulated "network delay" before firing callbacks.
const CALLBACK_DELAY_MS = 400;

function fireCallback(callback, data) {
  if (CALLBACK_DELAY_MS > 0) {
    setTimeout(() => callback(data), CALLBACK_DELAY_MS);
  } else {
    callback(data);
  }
}

